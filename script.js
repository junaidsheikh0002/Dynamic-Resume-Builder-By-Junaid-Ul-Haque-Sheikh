function generateResume() {
    document.getElementById("outputName").textContent = document.getElementById("name").value;
    document.getElementById("outputEmail").textContent = document.getElementById("email").value;
    document.getElementById("outputPhone").textContent = document.getElementById("phone").value;
    document.getElementById("outputEducation").textContent = document.getElementById("education").value;
    document.getElementById("outputExperience").textContent = document.getElementById("experience").value;
    document.getElementById("outputSkills").textContent = document.getElementById("skills").value;
}